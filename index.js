module.exports = {
    /**
     * suma = 1 + 2 = 3
     * @param {*} n1 Número uno de la suma
     * @param {*} n2 Número dos de la suma
     * @returns 
     */
    suma: function (n1, n2) {
        return n1 + n2;
    },
    /**
     * suma = 2 - 1 = 2
     * @param {*} n1 Número uno
     * @param {*} n2 Número dos
     * @returns 
     */
    resta: function (n1, n2) {
        return n1 - n2;
    },
    /**
     * suma = 2 * 1 = 2
     * @param {*} n1 Número uno
     * @param {*} n2 Número dos
     * @returns 
     */
    multiplicacion: function (n1, n2) {
        return n1 * n2;
    },
    /**
     * suma = 2 / 1 = 2
     * @param {*} n1 Número uno
     * @param {*} n2 Número dos
     * @returns 
     */
    division: function (n1, n2) {
        return n1 / n2;
    }
}